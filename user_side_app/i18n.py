def normalize_lang(lang: str | None, fallback: str = DEFAULT_LANG) -> str:
    if lang in TEXTS:
        return lang
    if lang in SUPPORTED_LANGS:
        return lang
    return fallback if fallback in TEXTS else DEFAULT_LANG


def tr(lang: str, key: str) -> str:
    safe = normalize_lang(lang)
    if key in TEXTS.get(safe, {}):
        return TEXTS[safe][key]
    if key in TEXTS[DEFAULT_LANG]:
        return TEXTS[DEFAULT_LANG][key]
    return TEXTS["ar"].get(key, key)
